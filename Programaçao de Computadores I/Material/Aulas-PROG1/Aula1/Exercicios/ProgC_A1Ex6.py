# # 6. Informe o tipo de cada dado a seguir e atribua esse dado a uma 
# variável com identificador valido em Python. Valores:

# A) 3245	
# B) ’a’
# C) 3.45221
# D) 5+3j

# A) 3245 → tipo: int (inteiro)
a = int(3245)  # variável válida do tipo inteiro

# B) 'a' → tipo: str (caractere/string)
b = str('a')  # variável válida do tipo string

# C) 3.45221 → tipo: float (ponto flutuante)
c = float(3.45221)  # variável válida do tipo float

# D) 5+3j → tipo: complex (número complexo)
d = complex(5, 3)  # variável válida do tipo complexo (parte real = 5, parte imaginária = 3j)
